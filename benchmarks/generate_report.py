def page_read_amplification(pdf: PdfPages, all_runs: dict[str, dict], finding: str = "",
                            what_why: tuple[str, str] | None = None) -> None:
    """Read amplification analysis — the key zarr2 vs zarr3 finding."""
    fig, (ax1, ax2) = plt.subplots(1, 2, figsize=(11, 8.5))
    fig.suptitle("Read Amplification: Disk Reads vs Actual Data Needed",
                 fontsize=16, fontweight="bold")

    if what_why:
        fig.text(0.5, 0.92, f"WHAT: {what_why[0]}\nWHY: {what_why[1]}",
                 ha="center", fontsize=7.5, style="italic", color="#555",
                 bbox=dict(boxstyle="round,pad=0.3", facecolor="#f5f5f5", alpha=0.7))

    labels = []
    display_labels = []
    data_needed = []
    disk_read = []
    amplifications = []

    # Display names for x-axis — "iso" is really "128³ patch" in this context
    _amp_display = {
        "patch64": "64³ patch\n(Jiefu+H01)",
        "iso": "128³ patch\n(Jiefu+H01)",
        "patch256": "256³ patch\n(Jiefu+H01)",
        "jiefu": "Jiefu\n(zarr2, 128³)",
        "h01": "H01\n(zarr3, 128³)",
    }

    for label in ["patch64", "iso", "patch256", "jiefu", "h01"]:
        if label not in all_runs:
            continue
        data = all_runs[label]
        bd = data.get("materialize_breakdown")
        if bd is None:
            continue
        avg_bytes = float(np.mean(data["read_bytes"])) / 1e6
        disk_mb = float(np.mean(bd["ts_file_bytes"])) / 1e6
        labels.append(label)
        display_labels.append(_amp_display.get(label, label))
        data_needed.append(avg_bytes)
        disk_read.append(disk_mb)
        amplifications.append(disk_mb / avg_bytes if avg_bytes > 0 else 0)

    if not labels:
        plt.close(fig)
        return

    x = np.arange(len(labels))
    w = 0.35

    # Left: stacked bar — data needed vs extra read
    extra = [d - n for d, n in zip(disk_read, data_needed)]
    ax1.bar(x, data_needed, w, label="Data actually needed", color="#4CAF50", alpha=0.8)
    ax1.bar(x, extra, w, bottom=data_needed, label="Wasted reads (amplification)",
            color="#F44336", alpha=0.6)
    for i in range(len(labels)):
        ax1.text(i, disk_read[i] + 1, f"{amplifications[i]:.1f}x",
                 ha="center", fontsize=10, fontweight="bold",
                 color="#F44336" if amplifications[i] > 2 else "#4CAF50")
    ax1.set_xticks(x)
    ax1.set_xticklabels(display_labels, fontsize=7.5)
    ax1.set_ylabel("MB per sample")
    ax1.set_title("Disk Read vs Data Needed")
    ax1.legend(fontsize=9)

    # Right: amplification factor bar chart (uniform color for clarity)
    bars = ax2.bar(x, amplifications, color="#2196F3", alpha=0.8, edgecolor="white")
    ax2.axhline(1.0, color="gray", linestyle="--", alpha=0.5, label="1.0x = no waste")
    for i, a in enumerate(amplifications):
        ax2.text(i, a + 0.3, f"{a:.1f}x", ha="center", fontsize=11, fontweight="bold")
    ax2.set_xticks(x)
    ax2.set_xticklabels(display_labels, fontsize=7.5)
    ax2.set_ylabel("Read amplification factor")
    ax2.set_title("Read Amplification (lower is better)")
    ax2.legend(fontsize=9)

    if finding:
        n_lines = finding.count("\n") + 1
        bottom_margin = min(0.05 + n_lines * 0.012, 0.30)
        top_margin = 0.90 if what_why else 0.93
        fig.text(0.5, 0.01, finding, ha="center", fontsize=7.5,
                 color="#333", wrap=True,
                 bbox=dict(boxstyle="round,pad=0.4", facecolor="#FFF9C4", alpha=0.9))
        fig.tight_layout(rect=[0, bottom_margin, 1, top_margin])
    else:
        note = ("Read amplification = (bytes read from disk) / (bytes actually needed). "
                "zarr2 chunk misalignment causes massive waste for small patches.")
        fig.text(0.5, 0.02, note, ha="center", fontsize=9, style="italic", color="#666")
        top_margin = 0.90 if what_why else 0.93
        fig.tight_layout(rect=[0, 0.05, 1, top_margin])
    pdf.savefig(fig, bbox_inches="tight")
    plt.close(fig)


