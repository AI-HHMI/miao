def _safe_float_col(rows: list[dict], col: str, default: float = 0.0) -> np.ndarray:
    """Read a float column from CSV rows, returning default if column missing."""
    if col not in rows[0]:
        return np.full(len(rows), default)
    return np.array([float(r.get(col, default)) for r in rows])


def _safe_int_col(rows: list[dict], col: str, default: int = 0) -> np.ndarray:
    """Read an int column from CSV rows, returning default if column missing."""
    if col not in rows[0]:
        return np.full(len(rows), default, dtype=int)
    return np.array([int(r.get(col, default)) for r in rows])


def load_stage_csv(path: str) -> dict:
    """Load per-sample stage timings from CSV.

    Returns dict with:
        stages: dict[stage_name -> np.ndarray of ms values]
        total: np.ndarray of total_ms
        read_bytes: np.ndarray of read_bytes_total
        n_samples: int
        label: str
        materialize_breakdown: dict (present if new columns exist)
    """
    rows = []
    with open(path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append(row)

    if not rows:
        raise ValueError(f"No data rows in {path}")

    stages = {}
    for col, key in zip(STAGE_COLUMNS, STAGE_KEYS):
        stages[key] = np.array([float(r[col]) for r in rows])

    total = np.array([float(r["total_ms"]) for r in rows])
    read_bytes = np.array([int(r["read_bytes_total"]) for r in rows])

    result = {
        "stages": stages,
        "total": total,
        "read_bytes": read_bytes,
        "n_samples": len(rows),
        "label": rows[0].get("label", ""),
    }

    # New columns from Phase 1 (backward-compatible: skip if absent)
    has_breakdown = "img_fr_ms_l0" in rows[0]
    if has_breakdown:
        result["materialize_breakdown"] = {
            # Approach A: per-level sub-timers
            "img_fr_ms": np.column_stack([
                _safe_float_col(rows, f"img_fr_ms_l{i}") for i in range(3)
            ]),
            "lbl_fr_ms": np.column_stack([
                _safe_float_col(rows, f"lbl_fr_ms_l{i}") for i in range(3)
            ]),
            "np_img_ms": np.column_stack([
                _safe_float_col(rows, f"np_img_ms_l{i}") for i in range(3)
            ]),
            "np_lbl_ms": np.column_stack([
                _safe_float_col(rows, f"np_lbl_ms_l{i}") for i in range(3)
            ]),
            # Approach B: CPU vs IO
            "cpu_ms": _safe_float_col(rows, "cpu_ms"),
            "io_wait_ms": _safe_float_col(rows, "io_wait_ms"),
            "majflt": _safe_int_col(rows, "majflt"),
            "minflt": _safe_int_col(rows, "minflt"),
            "utime_ms": _safe_float_col(rows, "utime_ms"),
            "stime_ms": _safe_float_col(rows, "stime_ms"),
            # Approach C: TensorStore metrics
            "ts_cache_hits": _safe_int_col(rows, "ts_cache_hits"),
            "ts_cache_misses": _safe_int_col(rows, "ts_cache_misses"),
            "ts_file_bytes": _safe_int_col(rows, "ts_file_bytes"),
            "ts_file_reads": _safe_int_col(rows, "ts_file_reads"),
            "ts_read_lat_ms": _safe_float_col(rows, "ts_read_lat_ms"),
            "ts_evicts": _safe_int_col(rows, "ts_evicts"),
        }
        # Volume names (for per-dataset filtering)
        result["volume_names"] = [r.get("volume_name", "") for r in rows]

    return result


def load_dataloader_csv(path: str) -> dict[int, dict]:
    """Load DataLoader throughput from CSV.

    Returns dict[num_workers -> {mean_ms, median_ms, p95_ms, samples_per_sec, n_batches}]
    """
    results = {}
    with open(path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            nw = int(row["num_workers"])
            results[nw] = {
                "mean_ms": float(row["mean_ms"]),
                "median_ms": float(row["median_ms"]),
                "p95_ms": float(row["p95_ms"]),
                "samples_per_sec": float(row["samples_per_sec"]),
                "n_batches": int(row["n_batches"]),
            }
    return results


def load_batch_sweep_csv(path: str) -> dict[int, dict[int, dict]]:
    """Load batch size sweep results from CSV.

    Returns dict[num_workers -> dict[batch_size -> {mean_ms, median_ms, p95_ms,
    samples_per_sec, n_batches}]].
    """
    results: dict[int, dict[int, dict]] = {}
    with open(path, newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            nw = int(row["num_workers"])
            bs = int(row["batch_size"])
            if nw not in results:
                results[nw] = {}
            results[nw][bs] = {
                "mean_ms": float(row["mean_ms"]),
                "median_ms": float(row["median_ms"]),
                "p95_ms": float(row["p95_ms"]),
                "samples_per_sec": float(row["samples_per_sec"]),
                "n_batches": int(row["n_batches"]),
            }
    return results


def load_metadata(path: str) -> dict:
    """Load run metadata from JSON."""
    with open(path) as f:
        return json.load(f)


def find_latest_date(results_dir: str) -> str:
    """Find the most recent results_YYYYMMDD.csv in the directory (backward compat)."""
    p = Path(results_dir)
    pattern = re.compile(r"results_(\d{8})\.csv$")
    dates = []
    for f in p.iterdir():
        if f.is_file():
            m = pattern.match(f.name)
            if m:
                dates.append(m.group(1))
    if not dates:
        raise FileNotFoundError(f"No results_YYYYMMDD.csv files found in {results_dir}")
    return sorted(dates)[-1]


def find_latest_run_dir(base_dir: str) -> Path:
    """Find the most recent run_YYYYMMDD/ subdirectory under base_dir.

    Falls back to base_dir itself if no run_* subdirectories exist.
    """
    p = Path(base_dir)
    pattern = re.compile(r"run_(\d{8})$")
    run_dirs = []
    for d in p.iterdir():
        if d.is_dir():
            m = pattern.match(d.name)
            if m:
                run_dirs.append((m.group(1), d))
    if run_dirs:
        return sorted(run_dirs)[-1][1]
    return p


def find_results_label(run_dir: Path) -> str:
    """Find the first results CSV label in a run directory.

    Looks for files matching results_{label}.csv (excluding _dataloader suffix).
    """
    pattern = re.compile(r"results_(.+)\.csv$")
    for f in sorted(run_dir.iterdir()):
        if not f.is_file():
            continue
        m = pattern.match(f.name)
        if m:
            suffix = m.group(1)
            if suffix.endswith("_dataloader") or suffix.endswith("_meta"):
                continue
            return suffix
    raise FileNotFoundError(f"No results_*.csv files found in {run_dir}")


