    for ds in datasets:
        row.append(f"{np.mean(ds['data']['total']):.1f} ms")
    rows.append(row)
    # Bytes per sample
    row = ["Bytes/sample"]
    for ds in datasets:
        row.append(f"{np.mean(ds['data']['read_bytes'])/1e6:.1f} MB")
    rows.append(row)
    # Config info
