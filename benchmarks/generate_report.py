def _resolve_input_paths(args) -> tuple[Path, str]:
    """Resolve results directory and label from CLI args.

    Handles three cases:
      1. --date given (backward compat): results_dir/results_{date}.csv
      2. --label given: results_dir/results_{label}.csv
      3. Neither: auto-detect latest run_YYYYMMDD/ dir, then first CSV label
    Returns (results_dir, file_stem) where files are results_{stem}[_dataloader|_meta].*
    """
    results_dir = Path(args.results_dir)

    if args.date:
        return results_dir, args.date

    if args.label:
        # If results_dir has run_YYYYMMDD subdirs, find latest
        try:
            run_dir = find_latest_run_dir(str(results_dir))
            if run_dir != results_dir:
                results_dir = run_dir
        except Exception:
            pass
        return results_dir, args.label

    # Auto-detect: first try run_YYYYMMDD dirs, then old format
    try:
        run_dir = find_latest_run_dir(str(results_dir))
        if run_dir != results_dir:
            results_dir = run_dir
            label = find_results_label(results_dir)
            print(f"Auto-detected run dir: {results_dir}")
            print(f"Auto-detected label: {label}")
            return results_dir, label
    except Exception:
        pass

    # Fall back to old date-based format
    try:
        date_str = find_latest_date(str(results_dir))
        print(f"Using most recent results (legacy format): {date_str}")
        return results_dir, date_str
    except FileNotFoundError:
        # Last resort: search for any results_*.csv
        label = find_results_label(results_dir)
        return results_dir, label


def _load_single_run(results_dir: Path, stem: str) -> tuple[dict, dict[int, dict], dict]:
    """Load stage CSV, dataloader CSV, and metadata for a single run."""
    stage_csv = results_dir / f"results_{stem}.csv"
    dl_csv = results_dir / f"results_{stem}_dataloader.csv"
    meta_json = results_dir / f"results_{stem}_meta.json"

    if not stage_csv.exists():
        raise FileNotFoundError(
            f"Stage timings CSV not found: {stage_csv}\n"
            f"Run profiling first: python benchmarks/profile_getitem.py --config examples/config.yaml"
        )

    print(f"Reading stage timings: {stage_csv}")
    data = load_stage_csv(str(stage_csv))
    print(f"  {data['n_samples']} samples loaded")

    dl_results = {}
    if dl_csv.exists():
        print(f"Reading DataLoader results: {dl_csv}")
        dl_results = load_dataloader_csv(str(dl_csv))
        print(f"  {len(dl_results)} worker configurations loaded")
    else:
        print(f"No DataLoader CSV found ({dl_csv}) — skipping page 4")

    meta = {}
    if meta_json.exists():
        print(f"Reading run metadata: {meta_json}")
        meta = load_metadata(str(meta_json))
    else:
        print(f"No metadata JSON found ({meta_json}) — using defaults")
        meta = {"label": data["label"], "startup_ms": 0, "config": {}, "timestamp": "unknown"}

    return data, dl_results, meta


def _generate_standard_pages(pdf: PdfPages, data: dict, dl_results: dict[int, dict],
                              meta: dict) -> None:
    """Generate the standard 7-page report."""
    page1_summary(pdf, data, meta)
    print("  Page 1: Executive summary")
    page2_breakdown(pdf, data)
    print("  Page 2: Stage breakdown")
    page3_distributions(pdf, data)
    print("  Page 3: Distributions")
    if dl_results:
        page4_dataloader(pdf, dl_results)
        print("  Page 4: DataLoader throughput")
    page5_timeline(pdf, data)
    print("  Page 5: Latency timeline")
    page6_alternatives(pdf)
    print("  Page 6: Alternative pipelines")
    if "materialize_breakdown" in data:
        page7_materialize_breakdown(pdf, data)
        print("  Page 7: Data materialize breakdown")
    else:
        print("  Page 7: Skipped (no breakdown columns in CSV)")


def main():
    parser = argparse.ArgumentParser(
        description="Generate PDF benchmark report from profiling CSV data",
    )
    parser.add_argument("--results_dir", default="benchmarks",
                        help="Directory containing results CSV files. "
                             "Auto-detects latest run_YYYYMMDD/ subdirectory (default: benchmarks/)")
    parser.add_argument("--date", default="",
                        help="Date string YYYYMMDD to select results (legacy format, backward compat)")
    parser.add_argument("--label", default="",
                        help="Label to select results files (e.g., 'config' for results_config.csv)")
    parser.add_argument("--compare", nargs="+", metavar="CSV",
                        help="Multiple CSV paths to generate comparison report (pages 8-9)")
    parser.add_argument("--consolidate", action="store_true",
                        help="Generate one consolidated PDF from ALL CSVs in the run directory")
    parser.add_argument("--output", default="",
                        help="Output PDF path (default: auto-generated in results_dir)")
    args = parser.parse_args()

    if args.consolidate:
        # === Consolidated mode ===
        results_dir = Path(args.results_dir)
        try:
            run_dir = find_latest_run_dir(str(results_dir))
        except Exception:
            run_dir = results_dir
        output_path = args.output or str(run_dir / "miao_consolidated_report.pdf")
        generate_consolidated_report(run_dir, output_path)
        print("Done.")
        return

    if args.compare:
        # === Comparison mode ===
        print(f"Comparison mode: {len(args.compare)} CSV files")
        datasets = []
        for csv_path in args.compare:
            p = Path(csv_path)
            if not p.exists():
                raise FileNotFoundError(f"CSV not found: {csv_path}")
            data = load_stage_csv(str(p))
            # Try loading corresponding meta JSON
            meta_path = p.parent / p.name.replace(".csv", "_meta.json")
            meta = {}
            if meta_path.exists():
                meta = load_metadata(str(meta_path))
            label = data.get("label", p.stem.replace("results_", ""))
            datasets.append({"data": data, "meta": meta, "label": label})
            print(f"  Loaded: {csv_path} ({data['n_samples']} samples, label='{label}')")

        output_path = args.output or str(
            Path(args.compare[0]).parent / "miao_comparison_report.pdf"
        )
        print(f"\nGenerating comparison report: {output_path}")
        with PdfPages(output_path) as pdf:
            # Generate standard pages for the first CSV
            first_data = datasets[0]["data"]
            first_meta = datasets[0]["meta"]
            dl_csv = Path(args.compare[0]).with_name(
                Path(args.compare[0]).name.replace(".csv", "_dataloader.csv")
            )
            dl_results = {}
            if dl_csv.exists():
                dl_results = load_dataloader_csv(str(dl_csv))
            _generate_standard_pages(pdf, first_data, dl_results, first_meta)

            # Comparison pages
            page8_dataset_comparison(pdf, datasets)
            print("  Page 8: Dataset/config comparison")
            page9_scaling(pdf, datasets)
            print("  Page 9: Scaling analysis")

            # Auto-detect cold/warm cache comparison
            cold_ds = [d for d in datasets if d["label"].endswith("_cold")]
            warm_ds = [d for d in datasets if d["label"].endswith("_warm")]
            if cold_ds and warm_ds:
                page10_cache_comparison(pdf, cold_ds[0]["data"], warm_ds[0]["data"])
                print("  Page 10: Cache comparison (cold vs warm)")

        print(f"\nComparison report saved to: {output_path}")

    else:
        # === Single-run mode ===
        results_dir, stem = _resolve_input_paths(args)
        data, dl_results, meta = _load_single_run(results_dir, stem)
        output_path = args.output or str(results_dir / f"miao_io_report_{stem}.pdf")

        print(f"\nGenerating PDF report: {output_path}")
        with PdfPages(output_path) as pdf:
            _generate_standard_pages(pdf, data, dl_results, meta)

        print(f"\nReport saved to: {output_path}")

    print("Done.")


if __name__ == "__main__":
    main()
