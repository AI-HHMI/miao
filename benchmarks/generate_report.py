        cold_vals = [float(np.mean(cold_bd[m])) for m in cache_metrics]
        warm_vals = [float(np.mean(warm_bd[m])) for m in cache_metrics]

        ax.bar(x - width / 2, cold_vals, width, label="Cold", color="#2196F3", alpha=0.8)
        ax.bar(x + width / 2, warm_vals, width, label="Warm", color="#4CAF50", alpha=0.8)
        for j in range(len(cache_labels)):
            ax.text(x[j] - width / 2, cold_vals[j] + 0.3, f"{cold_vals[j]:.1f}",
                    ha="center", fontsize=8)
            ax.text(x[j] + width / 2, warm_vals[j] + 0.3, f"{warm_vals[j]:.1f}",
                    ha="center", fontsize=8)
