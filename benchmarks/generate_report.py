def page7_materialize_breakdown(pdf: PdfPages, data: dict, finding: str = "",
                                title: str = "Disk Read & Decompress Breakdown",
                                what_why: tuple[str, str] | None = None,
                                y_max_levels: float = 0) -> None:
    """Page 7: Disk read & decompress breakdown — stacked time split, CPU/IO, cache, findings.

    Top-left:  Horizontal stacked bar — full breakdown of data_materialize time
               into per-level image/label components + overhead.
    Top-right: CPU vs IO Analysis — parallelism ratio, page faults.
    Bot-left:  TensorStore In-Memory Cache — hits/misses/evictions.
    Bot-right: Key Findings text panel (replaces old pie chart).
    """
    bd = data.get("materialize_breakdown")
    if bd is None:
        return

    fig, axes = plt.subplots(2, 2, figsize=(11, 8.5))
    fig.suptitle(title, fontsize=16, fontweight="bold")

    if what_why:
        fig.text(0.5, 0.92, f"WHAT: {what_why[0]}\nWHY: {what_why[1]}",
                 ha="center", fontsize=7.5, style="italic", color="#555",
                 bbox=dict(boxstyle="round,pad=0.3", facecolor="#f5f5f5", alpha=0.7))

    n_levels = bd["img_fr_ms"].shape[1]
    mat_mean = float(np.mean(data["stages"]["data_materialize"]))

    # Build per-component means and labels — use descriptive names
    component_names = []
    component_means = []
    component_colors = []
    img_palette = ["#E53935", "#EF5350", "#EF9A9A"]  # reds — L0 dark to L2 light
    lbl_palette = ["#1E88E5", "#42A5F5", "#90CAF9"]  # blues — L0 dark to L2 light
    for i in range(n_levels):
        val = float(np.mean(bd["img_fr_ms"][:, i]))
        lbl_text = LEVEL_LABELS[i] if i < len(LEVEL_LABELS) else f"L{i}"
        component_names.append(f"Image {lbl_text}")
        component_means.append(val)
        component_colors.append(img_palette[min(i, len(img_palette) - 1)])
    for i in range(n_levels):
        val = float(np.mean(bd["lbl_fr_ms"][:, i]))
        lbl_text = LEVEL_LABELS[i] if i < len(LEVEL_LABELS) else f"L{i}"
        component_names.append(f"Label {lbl_text}")
        component_means.append(val)
        component_colors.append(lbl_palette[min(i, len(lbl_palette) - 1)])

    accounted = sum(component_means)
    overhead = max(mat_mean - accounted, 0)
    if overhead > mat_mean * 0.005:  # only show if > 0.5%
        component_names.append("Other")
        component_means.append(overhead)
        component_colors.append("#BDBDBD")

    # Short names for stacked bar labels (fit in bar segments)
    short_names = []
    for i in range(n_levels):
        short_names.append(f"Img {LEVEL_LABELS_SHORT[i] if i < len(LEVEL_LABELS_SHORT) else f'L{i}'}")
    for i in range(n_levels):
        short_names.append(f"Lbl {LEVEL_LABELS_SHORT[i] if i < len(LEVEL_LABELS_SHORT) else f'L{i}'}")
    if overhead > mat_mean * 0.005:
        short_names.append("Other")

    # --- Top-left: horizontal stacked bar — full time breakdown ---
    ax = axes[0][0]
    left = 0
    for name, sname, val, color in zip(component_names, short_names, component_means, component_colors):
        ax.barh(0, val, left=left, height=0.5, color=color, edgecolor="white",
                linewidth=0.5, label=sname)
        pct = val / mat_mean * 100 if mat_mean > 0 else 0
        if pct > 5:
            ax.text(left + val / 2, 0, f"{val:.0f}ms\n({pct:.0f}%)",
                    ha="center", va="center", fontsize=8, fontweight="bold",
                    color="white" if val / mat_mean > 0.15 else "black")
        left += val

    ax.set_xlim(0, mat_mean * 1.05)
    ax.set_yticks([])
    ax.set_xlabel("Time (ms)")
    ax.set_title(f"Where Does the {mat_mean:.0f}ms Go?", fontsize=11, fontweight="bold")
    ax.legend(fontsize=6.5, ncol=4, loc="upper center",
              bbox_to_anchor=(0.5, -0.08), framealpha=0.9)

    # Annotate dominant component
    max_idx = int(np.argmax(component_means))
    dom_name = short_names[max_idx]
    dom_val = component_means[max_idx]
    dom_pct = dom_val / mat_mean * 100 if mat_mean > 0 else 0
    ax.text(0.98, 0.95,
            f"Dominant: {dom_name}\n{dom_val:.0f}ms ({dom_pct:.0f}%)",
            transform=ax.transAxes, fontsize=9, fontweight="bold",
            ha="right", va="top", color="#B71C1C",
            bbox=dict(boxstyle="round,pad=0.3", facecolor="#ffebee", alpha=0.9))

    # --- Top-right: CPU vs IO Analysis ---
    ax = axes[0][1]
    cpu_mean = float(np.mean(bd["cpu_ms"]))
    majflt_mean = float(np.mean(bd["majflt"]))
    minflt_mean = float(np.mean(bd["minflt"]))
    utime_mean = float(np.mean(bd["utime_ms"]))
    stime_mean = float(np.mean(bd["stime_ms"]))
    parallelism = cpu_mean / mat_mean if mat_mean > 0 else 0

    ax.axis("off")
    ax.set_title("CPU vs IO Analysis", fontsize=10, fontweight="bold")

    info_text = (
        f"Wall time (disk read+decomp):  {mat_mean:.1f} ms\n"
        f"CPU time (all threads):        {cpu_mean:.1f} ms\n"
        f"Multi-thread Decompression:    {parallelism:.1f}x\n"
        f"  (TensorStore uses {parallelism:.0f} CPU cores at once\n"
        f"   to decompress chunks in parallel)\n"
        f"\n"
        f"User CPU:   {utime_mean:.1f} ms  (decompression)\n"
        f"System CPU: {stime_mean:.1f} ms  (kernel/IO)\n"
        f"\n"
        f"Major page faults: {majflt_mean:.1f}/sample\n"
        f"  (data fetched from NFS — 0 means cached)\n"
        f"Minor page faults: {minflt_mean:.0f}/sample\n"
        f"  (data served from OS page cache — fast)\n"
        f"\n"
    )
    if parallelism > 1.5:
        info_text += (
            f"INTERPRETATION:\n"
            f"CPU time exceeds wall time because\n"
            f"{parallelism:.0f} cores decompress simultaneously.\n"
            f"Bottleneck: CPU decompression,\n"
            f"not NFS network transfer."
        )
    elif majflt_mean > 1:
        info_text += (
            f"INTERPRETATION:\n"
            f"High major page faults = data fetched\n"
            f"from NFS (not in OS cache).\n"
            f"Bottleneck is network IO."
        )
    else:
        info_text += (
            f"INTERPRETATION:\n"
            f"Low faults + moderate CPU = data served\n"
            f"from OS page cache (not from NFS)."
        )

    ax.text(0.05, 0.95, info_text, transform=ax.transAxes, fontsize=8.5,
            family="monospace", va="top",
            bbox=dict(boxstyle="round,pad=0.5", facecolor="#e3f2fd", alpha=0.9))

    # --- Bottom-left: TensorStore In-Memory Cache ---
    ax = axes[1][0]
    hits_mean = float(np.mean(bd["ts_cache_hits"]))
    misses_mean = float(np.mean(bd["ts_cache_misses"]))
    evicts_mean = float(np.mean(bd["ts_evicts"]))

    cache_labels_list = [
        "Hits\n(found in cache)",
        "Misses\n(read from disk)",
        "Evictions\n(removed from cache)",
    ]
    cache_vals = [hits_mean, misses_mean, evicts_mean]
    cache_colors = ["#4CAF50", "#F44336", "#FF9800"]
    bars = ax.bar(cache_labels_list, cache_vals, color=cache_colors, alpha=0.8, edgecolor="white")
    for bar, val in zip(bars, cache_vals):
        ax.text(bar.get_x() + bar.get_width() / 2, bar.get_height() + 0.3,
                f"{val:.1f}", ha="center", fontsize=10, fontweight="bold")

    ax.set_ylabel("Count per sample")
    ax.set_title("TensorStore In-Memory Cache (1 GB)")
    ax.set_ylim(0, max(cache_vals) * 1.25 if max(cache_vals) > 0 else 1)
    ax.tick_params(axis='x', labelsize=7.5)

    if hits_mean + misses_mean > 0:
        hit_rate = hits_mean / (hits_mean + misses_mean) * 100
        ax.text(0.98, 0.95, f"Hit rate: {hit_rate:.0f}%", transform=ax.transAxes,
                fontsize=11, fontweight="bold", ha="right", va="top",
                color="#4CAF50" if hit_rate > 50 else "#F44336")

    # --- Bottom-right: Key Findings text panel (replaces pie chart) ---
    ax = axes[1][1]
    ax.axis("off")
    ax.set_title("Key Findings", fontsize=10, fontweight="bold")

    ts_bytes_mean = float(np.mean(bd["ts_file_bytes"])) / 1e6  # MB
    ts_reads_mean = float(np.mean(bd["ts_file_reads"]))
    ts_lat_mean = float(np.mean(bd["ts_read_lat_ms"]))
    data_bytes_mean = float(np.mean(data["read_bytes"])) / 1e6  # MB
    amplif = ts_bytes_mean / data_bytes_mean if data_bytes_mean > 0 else 0
    hit_rate = hits_mean / (hits_mean + misses_mean) * 100 if (hits_mean + misses_mean) > 0 else 0

    # Identify dominant component
    img_total = sum(component_means[:n_levels])
    lbl_total = sum(component_means[n_levels:2*n_levels])
    img_pct = img_total / mat_mean * 100 if mat_mean > 0 else 0
    lbl_pct = lbl_total / mat_mean * 100 if mat_mean > 0 else 0

    findings_text = (
        f"STACKED BAR (top-left):\n"
        f"  Image: {img_total:.0f}ms ({img_pct:.0f}%), "
        f"Label: {lbl_total:.0f}ms ({lbl_pct:.0f}%)\n"
        f"  L0 (full res) dominates — it has the\n"
        f"  most voxels to read and decompress.\n"
        f"\n"
        f"CPU vs IO (top-right):\n"
        f"  {parallelism:.0f}x multi-thread decompression =\n"
        f"  {parallelism:.0f} CPU cores decompress at once.\n"
    )
    if parallelism > 1.5:
        findings_text += "  Bottleneck = CPU decompression.\n"
    else:
        findings_text += "  Bottleneck = NFS network IO.\n"
    findings_text += (
        f"\n"
        f"CACHE (bottom-left):\n"
        f"  {hit_rate:.0f}% hit rate (hits = found in\n"
        f"  cache, misses = had to read from disk,\n"
        f"  evictions = old data removed for new). "
    )
    if evicts_mean > misses_mean:
        findings_text += (
            f"\n"
            f"  {evicts_mean:.0f} evictions > {misses_mean:.0f} misses\n"
            f"  = cache is full, constantly replacing\n"
            f"  data that won't be reused.\n"
        )
    elif hit_rate < 20:
        findings_text += (
            f"\n"
            f"  Very low — random sampling means each\n"
            f"  read hits different chunks.\n"
        )
    else:
        findings_text += (
            f"\n"
            f"  Some reuse across resolution levels.\n"
        )
    findings_text += (
        f"\n"
        f"DISK IO STATS:\n"
        f"  Disk: {ts_bytes_mean:.1f} MB, Need: {data_bytes_mean:.1f} MB\n"
        f"  Amplification: {amplif:.1f}x "
    )
    if amplif > 5:
        findings_text += "(massive waste)"
    elif amplif > 2:
        findings_text += "(moderate waste)"
    else:
        findings_text += "(well-aligned)"

    ax.text(0.05, 0.95, findings_text, transform=ax.transAxes, fontsize=8,
            family="monospace", va="top",
            bbox=dict(boxstyle="round,pad=0.5", facecolor="#fff3e0", alpha=0.9))

    if finding:
        n_lines = finding.count("\n") + 1
        bottom_margin = min(0.04 + n_lines * 0.013, 0.30)
        fig.text(0.5, 0.01, finding, ha="center", fontsize=7, style="italic",
                 color="#333", wrap=True,
                 bbox=dict(boxstyle="round,pad=0.3", facecolor="#FFF9C4", alpha=0.9))
        fig.tight_layout(rect=[0, bottom_margin, 1, 0.90])
    else:
        fig.tight_layout(rect=[0, 0, 1, 0.90])
    pdf.savefig(fig, bbox_inches="tight")
    plt.close(fig)


