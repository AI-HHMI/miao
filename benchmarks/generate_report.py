                    f"helped, warm should be noticeably faster. It is not — both are nearly identical.\n"
                    f"Distribution (top-right): Disk Read & Decompress histograms overlap — "
                    f"warm was {faster_or_slower} ({warm_mat:.0f}ms vs cold {cold_mat:.0f}ms), well within "
                    f"normal run-to-run variation. No real improvement from having a warm cache.\n"
                    f"WHY: Random sampling picks patches from random locations in large volumes. "
                    f"Each new patch reads different chunks. By the time a chunk might be needed again, "
