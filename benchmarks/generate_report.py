    ax.set_title("Cache Effect Summary", fontsize=10, fontweight="bold")

    cold_total = np.mean(cold_data["total"])
    warm_total = np.mean(warm_data["total"])
    cold_mat_mean = np.mean(cold_mat)
    warm_mat_mean = np.mean(warm_mat)
