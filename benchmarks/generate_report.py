    ax = axes[1][0]
    cold_bd = cold_data.get("materialize_breakdown")
    warm_bd = warm_data.get("materialize_breakdown")

    if cold_bd is not None and warm_bd is not None:
        cache_metrics = ["ts_cache_hits", "ts_cache_misses", "ts_evicts"]
        cache_labels = ["Hits", "Misses", "Evictions"]
